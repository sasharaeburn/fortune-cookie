let crackButton = document.getElementById('crack-button');

crackButton.addEventListener('click', event => {
    event.preventDefault();

    createFortuneCard();
})

function createFortuneCard() {
    let fortuneContainer = document.getElementById('fortune-container');
    fortuneContainer.innerHTML = '';

    let fortune = document.createElement('div');
    fortune.setAttribute('class', 'card');

    let fortuneText = document.createElement('div');
    fortuneText.setAttribute('class', 'card-body');

    let randomFortune = Math.floor(Math.random() * (fortunes.length));
    fortuneText.innerHTML = fortunes[randomFortune];

    fortune.appendChild(fortuneText);

    fortuneContainer.appendChild(fortune);
}

